create extension if not exists pgcrypto;

create type public.user_role as enum ('requester', 'mover', 'admin');
create type public.request_priority as enum ('normal', 'urgent');
create type public.request_status as enum (
  'pending', 'approved', 'assigned', 'picked_up', 'in_transit',
  'delivered', 'return_required', 'completed', 'cancelled'
);

create sequence public.request_code_seq start 1001;

create table public.sites (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  short_name text not null,
  address text not null,
  maps_query text,
  contact_name text,
  contact_phone text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null,
  full_name text not null,
  role public.user_role not null default 'requester',
  site_id uuid references public.sites(id) on delete set null,
  phone text,
  active boolean not null default true,
  must_change_password boolean not null default true,
  last_access_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint profiles_email_lowercase check (email = lower(email))
);
create unique index profiles_email_unique_idx on public.profiles (lower(email));

create table public.equipment (
  id uuid primary key default gen_random_uuid(),
  inventory_code text not null unique,
  name text not null,
  brand text,
  model text,
  serial_number text,
  home_site_id uuid not null references public.sites(id),
  current_site_id uuid not null references public.sites(id),
  movable boolean not null default true,
  active boolean not null default true,
  accessories text[] not null default '{}',
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.delivery_requests (
  id uuid primary key default gen_random_uuid(),
  code text not null unique default ('MM-' || nextval('public.request_code_seq')::text),
  equipment_id uuid not null references public.equipment(id),
  requester_id uuid not null references public.profiles(id),
  pickup_site_id uuid not null references public.sites(id),
  destination_site_id uuid not null references public.sites(id),
  assigned_mover_id uuid references public.profiles(id),
  assigned_mover_ids uuid[] not null default '{}',
  requested_date date not null,
  requested_time time not null,
  return_date date,
  return_time time,
  priority public.request_priority not null default 'normal',
  status public.request_status not null default 'pending',
  note text,
  cancelled_reason text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint different_sites check (pickup_site_id <> destination_site_id),
  constraint return_time_requires_date check (return_time is null or return_date is not null),
  constraint note_length check (note is not null and length(btrim(note)) between 3 and 1500)
);

create table public.request_events (
  id bigint generated by default as identity primary key,
  request_id uuid not null references public.delivery_requests(id) on delete cascade,
  actor_id uuid references public.profiles(id) on delete set null,
  event_type text not null,
