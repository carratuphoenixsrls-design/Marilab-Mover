create or replace function public.create_delivery_request(
  p_equipment_id uuid,
  p_pickup_site_id uuid,
  p_destination_site_id uuid,
  p_requested_date date,
  p_requested_time time,
  p_return_date date default null,
  p_return_time time default null,
  p_priority public.request_priority default 'normal',
  p_note text default null
)
returns public.delivery_requests
language plpgsql security definer set search_path = '' as $$
declare created public.delivery_requests;
begin
  if not public.is_active_user() then raise exception 'Account non autorizzato'; end if;
  if p_pickup_site_id = p_destination_site_id then raise exception 'Ritiro e destinazione devono essere diversi'; end if;
  if not exists(select 1 from public.equipment e where e.id = p_equipment_id and e.active and e.movable) then raise exception 'Apparecchiatura non disponibile'; end if;
  insert into public.delivery_requests (
    equipment_id, requester_id, pickup_site_id, destination_site_id,
    requested_date, requested_time, return_date, return_time, priority, note
  ) values (
    p_equipment_id, auth.uid(), p_pickup_site_id, p_destination_site_id,
    p_requested_date, p_requested_time, p_return_date, p_return_time, p_priority, nullif(btrim(p_note), '')
  ) returning * into created;
  return created;
end;
$$;

create or replace function public.assign_delivery_mover(p_request_id uuid, p_mover_id uuid)
returns public.delivery_requests
language plpgsql security definer set search_path = '' as $$
declare updated public.delivery_requests;
begin
  if not public.is_admin() then raise exception 'Funzione riservata agli Admin'; end if;
  if not exists(select 1 from public.profiles p where p.id = p_mover_id and p.role = 'mover' and p.active) then raise exception 'Mover non valido o non attivo'; end if;
  update public.delivery_requests
  set assigned_mover_id = p_mover_id,
      status = case when status in ('pending', 'approved') then 'assigned' else status end
  where id = p_request_id and status <> 'cancelled'
  returning * into updated;
  if updated.id is null then raise exception 'Richiesta non trovata'; end if;
  return updated;
end;
$$;

create or replace function public.update_delivery_status(p_request_id uuid, p_status public.request_status)
returns public.delivery_requests
language plpgsql security definer set search_path = '' as $$
declare current_request public.delivery_requests;
declare updated public.delivery_requests;
begin
  select * into current_request from public.delivery_requests where id = p_request_id for update;
  if current_request.id is null then raise exception 'Richiesta non trovata'; end if;
  if not public.is_admin() then
    if public.current_role() <> 'mover' or current_request.assigned_mover_id is distinct from auth.uid() then
      raise exception 'Puoi aggiornare soltanto le consegne assegnate a te';
    end if;
    if not (
      (current_request.status = 'assigned' and p_status = 'picked_up') or
      (current_request.status = 'picked_up' and p_status = 'in_transit') or
      (current_request.status = 'in_transit' and p_status in ('delivered', 'completed')) or
      (current_request.status = 'delivered' and p_status in ('return_required', 'completed')) or
      (current_request.status = 'return_required' and p_status = 'completed')
    ) then raise exception 'Passaggio di stato non consentito'; end if;
  end if;

  update public.delivery_requests set status = p_status where id = p_request_id returning * into updated;

  if p_status = 'delivered' then
    update public.equipment set current_site_id = updated.destination_site_id where id = updated.equipment_id;
  elsif p_status = 'completed' then
    update public.equipment
    set current_site_id = updated.destination_site_id
    where id = updated.equipment_id;
  end if;
  return updated;
end;
$$;

create or replace function public.create_app_notification(
  p_kind text,
  p_title text,
  p_body text,
  p_request_id uuid default null
)
returns bigint language plpgsql security definer set search_path = '' as $$
declare notification_id bigint;
begin
