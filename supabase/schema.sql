create or replace function public.log_request_change()
returns trigger language plpgsql security definer set search_path = '' as $$
begin
  if tg_op = 'INSERT' then
    insert into public.request_events (request_id, actor_id, event_type, new_status)
    values (new.id, auth.uid(), 'created', new.status);
  elsif old.status is distinct from new.status then
    insert into public.request_events (request_id, actor_id, event_type, previous_status, new_status)
    values (new.id, auth.uid(), 'status_changed', old.status, new.status);
  elsif old.assigned_mover_id is distinct from new.assigned_mover_id then
    insert into public.request_events (request_id, actor_id, event_type, metadata)
    values (new.id, auth.uid(), 'mover_assigned', jsonb_build_object('assigned_mover_id', new.assigned_mover_id));
  end if;
  return new;
end;
$$;

